"""
RGBT-Tiny dataset loader.
Uses MultiModalDroneDataset in file mode with a JSON manifest.
"""

import os
import sys
import json

if __package__ is None or __package__ == "":
    sys.path.insert(0, os.path.dirname(os.path.dirname(os.path.abspath(__file__))))

from datasets.multimodal_drone import MultiModalDroneDataset, collate_multimodal


def get_rgbt_tiny_dataset(manifest_path, transform=None):
    """
    Create a MultiModalDroneDataset for RGBT-Tiny.

    Args:
        manifest_path: path to the JSON manifest generated by scripts/clean_rgbt_tiny.py
        transform: DataAugmentationDINO_RGBIR instance

    Returns:
        MultiModalDroneDataset instance
    """
    assert os.path.exists(manifest_path), \
        f"Manifest not found: {manifest_path}. Run scripts/clean_rgbt_tiny.py first."

    dataset = MultiModalDroneDataset(
        data_source=manifest_path,
        transform=transform,
        mode='file',
    )

    return dataset


def get_rgbt_tiny_info(manifest_path):
    """Load manifest and print dataset statistics."""
    with open(manifest_path, 'r') as f:
        manifest = json.load(f)

    samples = manifest['samples']
    n_total = len(samples)

    # Count by type
    type_counts = {}
    seq_counts = {}
    for s in samples:
        t = s.get('type', 'unknown')
        type_counts[t] = type_counts.get(t, 0) + 1
        seq = s.get('sequence', 'unknown')
        seq_counts[seq] = seq_counts.get(seq, 0) + 1

    print(f"RGBT-Tiny dataset: {n_total} samples")
    print(f"  Types: {type_counts}")
    print(f"  Sequences: {len(seq_counts)}")

    return manifest
